/**
 * OOPS Concept
 *
 * Object-Oriented Programming (OOP) is a programming paradigm that uses objects and classes to structure software programs.
 * It is based on several key principles:
 *
 * 1. **Encapsulation**: Bundling the data (attributes) and methods (functions) that operate on the data into a single unit or class.
 *    It restricts direct access to some of an object's components, which can prevent the accidental modification of data.
 *
 * 2. **Abstraction**: Hiding the complex implementation details and showing only the necessary features of an object.
 *    It helps in reducing programming complexity and effort.
 *
 * 3. **Inheritance**: A mechanism where a new class inherits properties and behavior (methods) from an existing class.
 *    It promotes code reusability and establishes a natural hierarchy between classes.
 *
 * 4. **Polymorphism**: The ability of different objects to respond to the same function call in different ways.
 *    It allows methods to do different things based on the object it is acting upon, enhancing flexibility and integration.
 */
